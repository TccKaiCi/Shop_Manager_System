        ChiTietKMDTO km  = new ChiTietKMDTO();
        
        // xóa giá cũ
        km = list_ChiTietKM.getInfor(cthd.getStrMaGiay()
                        , strMaKM_Cu);
        
        tinhTongTien(cthd.getiSoLuong(), cthd.getiGiaBan()
                , km.getTiLeKM(), false
        );
        
        // cập nhật lại giá mới
