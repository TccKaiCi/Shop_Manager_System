    /**
     * tong tien = tong tien + So Luong * Gia Ban * ti le khuyen mai
     * @param iSoLuong
     * @param iGiaBan
     * @param tiLeKM
     * @param hanhDong true nếu là xóa và false nếu là thêm
     */
    private double tinhTongTien(int iSoLuong, int iGiaBan, boolean hanhDong) {
        if (hanhDong)
            TongTien += iSoLuong * iGiaBan;
        else
             TongTien -= iSoLuong * iGiaBan;
        return TongTien;
    }
    
    
